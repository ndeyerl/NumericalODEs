function [u, utrue, maxerr, t] = ARKERKDIRK(AE, AI, b, c, s, h, range, eps, pnum)
if(pnum==1) %kaps problem
    sz = 2;
    nsfunc = @(z,t)fkapns(z,t);
    sfunc = @(e,z,t)fkaps(e,z,t);
    truesoln = @(t)fkaptrue(t);
    ic = [1;1];
elseif(pnum==2) %prothero robinson trig problem
    sz = 1;
    nsfunc = @(z,t)fprtrigns(z,t);
    sfunc = @(e,z,t)fprtrigs(e,z,t);
    ic =1;
    truesoln = @(t)fprtrigtrue(t); 
elseif(pnum==3) %hw3 prob 5 variable stiffness scalar ode
    sz = 1;
    nsfunc = @(z,t)fhwns(z,t);
    sfunc = @(eps,z,t)fhws(eps,z,t);
    ic = [0];
    truesoln = @(t)fhwtrue(t);
elseif(pnum==4) %(8.8) scalar ode
    sz = 1;
    nsfunc = @(z,t)f88ns(z,t);
    sfunc = @(eps,z,t)f88s(eps,z,t);
    ic = [1];
    truesoln = @(t)f88true(t);
elseif(pnum==200) %vdp problem
    nsfunc = @(z,t)fvdpns(z,t);
    sfunc = @(e,z,t)fvdps(e,z,t);
    ic = [2,-0.6666654321121172];
    truesoln = @(t)fkaptrue(t); %placeholder->no analytic solution, but can compare w/
%[t,y] = ode15s(@(t,y) vdpfull(t,y,eps),[0 10],[2,-0.6666654321121172]);
%plot(t,y(:,1))    
end
n = round((range(2)-range(1))/h); %number of steps
u = zeros(round(sz),n); %2 rows by n colns->holds solution
utrue = zeros(round(sz),n); % holds true soln
t = zeros(1,n);
u(:,1) = ic; %set initial condition
utrue(:,1) = ic;
t(1) = range(1); %set initial time
for i=1:n-1
    %fprintf('main loop\n');
    t(i+1) = t(i) + h;
    %fprintf('t = %d\n',t);
    z = ARKERKDIRKstages(eps, s, AE, AI, c, h, u(:,i), t(i), pnum, sz);
    sum = zeros(round(sz),1);
    for j=1:s
        fns = nsfunc(z(:,j),t(i)+c(j)*h);
        fs = sfunc(eps,z(:,j),t(i)+c(j)*h);
        sum = sum + b(j)*fns + b(j)*fs;
    end
    u(:,i+1) = u(:,i) + h*sum;
    utrue(:,i+1) = truesoln(t(i+1));
end

    

err = zeros(round(sz),n);
for j = 1:n
    err(:,j) = abs(u(:,j)-utrue(:,j));
end
maxerr = max(err(:));
end
